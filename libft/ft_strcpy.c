#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	if (src)
	{
		i = 0;
		while (src[i])
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	else if (src[0] == '\0')
		dst[0] = '\0';
	return (dst);
}
